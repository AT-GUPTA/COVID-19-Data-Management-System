# ----------------------------CREATES---------------------------------------------------

CREATE TABLE Region
(
    rName VARCHAR(50) PRIMARY KEY
);

CREATE TABLE ProStaTer
(
    prostater      VARCHAR(50) PRIMARY KEY,
    cName          VARCHAR(50),
    rName          VARCHAR(50),
    agencyName     VARCHAR(100),
    population     INT,
    vaccPopulation INT,
    deaths         INT,
    unvaccInfected INT,
    vacc_Died      INT
);

CREATE TABLE Vaccine
(
    vName           VARCHAR(60),
    prostater       VARCHAR(50),
    totalVaccinated INT,
    vaccInfected    INT,
    vaccDeath       INT,
    PRIMARY KEY (vName, prostater),
    FOREIGN KEY (prostater) REFERENCES ProStaTer (prostater)
);

CREATE TABLE `User`
(
    uID              INT AUTO_INCREMENT PRIMARY KEY,
    userType         VARCHAR(50),
    fName            VARCHAR(100),
    lName            VARCHAR(100),
    citizenship      VARCHAR(50),
    emailAddress     VARCHAR(100),
    phoneNumber      VARCHAR(20),
    organizationName VARCHAR(100),
    dateOfBirth      DATE,
    accStatus        ENUM ('Active', 'Suspended'),
    suspensionDate   DATE
);

CREATE TABLE Organization
(
    oID              INT AUTO_INCREMENT PRIMARY KEY,
    cName            VARCHAR(50),
    organizationType VARCHAR(50),
    organizationName VARCHAR(100),
    delegateUID      INT,
    FOREIGN KEY (delegateUID) REFERENCES User (uID)
);

CREATE TABLE Researcher
(
    rID INT AUTO_INCREMENT PRIMARY KEY,
    uID INT,
    FOREIGN KEY (uID) REFERENCES User (uID)
);

CREATE TABLE Author
(
    authID INT PRIMARY KEY AUTO_INCREMENT,
    oID    INT,
    rID    INT,
    FOREIGN KEY (oID) REFERENCES Organization (oID),
    FOREIGN KEY (rID) REFERENCES Researcher (rID)
);

CREATE TABLE Article
(
    artID           INT AUTO_INCREMENT PRIMARY KEY,
    publicationDate DATE,
    majorTopic      VARCHAR(100),
    minorTopic      VARCHAR(100),
    summary         VARCHAR(1000),
    article         BLOB(5000),
    authType        VARCHAR(30),
    authID          INT,
    artStatus       ENUM ('displayed', 'removed'),
    removalDate     DATE,
    FOREIGN KEY (authID) REFERENCES Author (authID)
);

CREATE TABLE `Update`
(
    updateID      INT PRIMARY KEY AUTO_INCREMENT,
    prostater     VARCHAR(50),
    cName         VARCHAR(50),
    updateDate    DATE,
    updPopulation  INT,
    updVaccinated INT,
    updInfected   INT,
    updDeaths     INT,
    updVaccDied   INT,
    pfizerVacc    INT,
    pfizerDied    INT,
    AstraZenVacc  INT,
    AstraZenDied  INT,
    modernaVacc   INT,
    modernaDied   INT,
    jjVacc        INT,
    jjDied        INT
);


CREATE TABLE Special_User
(
    uID      INT,
    userName VARCHAR(100),
    password VARCHAR(50),
    FOREIGN KEY (uID) REFERENCES User (uID)
);

CREATE TABLE Author_Subs
(
    authID INT,
    uID    INT,
    FOREIGN KEY (authID) REFERENCES Author (authID),
    FOREIGN KEY (uID) REFERENCES User (uID)
);

CREATE TABLE Sent_Emails
(
    artID       INT,
    date_time   DATETIME,
    receiverUID INT,
    Subject     VARCHAR(200),
    FOREIGN KEY (artID) REFERENCES Article (artID),
    FOREIGN KEY (receiverUID) REFERENCES User (uID)
);

